/* Write three binary functions add, sub, mul that take two numbers and return their sum difference and product */

function add(a,b) {
	return a + b;
}

function sub(a,b) {
	return a-b;
}

function mul(a,b) {
	return a * b;
}

console.log(add(3,5));
console.log(sub(5,4));
console.log(mul(4,6));



